// Arithmetic Operators + - * / % ++ -- **
console.log(10 + 5 * 2)

// Comparison Operators == === != !== > < >= <=
//ผลที่ได้จะเป็น boolean คือ true กับ false , หรือ 0 กับ 1
console.log(10 == '10') //true
console.log(10 === '10') //false

console.log('Sombat >= Somjai')
console.log('Thailand' < 'Thaipia')

// Logical Operators &&  ||  !
//เอา True กับ false มากระทำกันผลที่ได้มีแค่ true หรือ false
console.log(true && true)
console.log(true && false)
console.log(false && true)
console.log(false && false)
console.log('------------------------')
console.log(true || false)
console.log(true || true)
console.log(false || true)
console.log(false || false)
console.log('------------------------')
console.log(!true)
console.log(!false)
console.log('------------------------')
console.log(7 < 9 && 10 + 3 * 2 > 5)


